// rules for naming variables

// you can't start with number
// example :-
// 12value (inValid)
// value1 (valid)

var value1 = 2;
// ** is used to calculate the power of variable
// eg.,
// value ** 2 == its square
// value ** 3 == its cube 
console.log(value1 ** 3 );


// you can use only underscore _ or dollar $ symbol
// first_name (valid)
// _firstname (valid)

// first$name (valid)
// $firstname (valid)


// you can't use spaces between the variable name
// eg.,
// var first name = "Nayan" (invalid)

// so we can use underScore between it like-
// it is called as snake_case writing
// var first_name = "Nayan" (valid)
// Or
// we can use camelCase writing
// where 1st letter is small and after that 
// 1st letter of new word is Capital
// var firstName = "Nayan" (valid)

// convention
// start with small letter and use camelCase writing
// this not the rule but just a most popular way of
// writing variables
// name of class can be start with Capital


