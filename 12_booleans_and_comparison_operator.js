// booleans and comparision operator

let num1 = 3;
let num2 = 4;
console.log(num1>num2);

num1 = 7;
num2 = 2;
console.log(num1<=num2);

num1 = "7";
num2 = 7;
console.log(num1>=num2);


// == VS ===
// == only checks the value is it same or not
console.log( num1 == num2 );

// == checks the value as well as data Type
// is it same or not
console.log( num1 === num2 );


// != vs !==
// != only checks the value is it same or not
console.log( num1 != num2 );

// !== only checks the value as well as
// data type is it same or not
console.log( num1 !== num2 );
















